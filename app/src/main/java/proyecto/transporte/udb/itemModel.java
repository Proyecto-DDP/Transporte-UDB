package proyecto.transporte.udb;

public class itemModel {
    int image;
    int status;
    String routeN;

    public int getImage(){
        return image;
    }
    public void setImage(int image){
        this.image = image;
    }
    public int getStatus(){
        return status;
    }
    public void setStatus(int status){
        this.status = status;
    }
    public String getRouteN(){
        return routeN;
    }
    public void setRouteN(String routeN){
        this.routeN = routeN;
    }
}
